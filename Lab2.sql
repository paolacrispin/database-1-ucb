CREATE TABLE CONCIERTO(
ID           INT IDENTITY (1,1) NOT NULL PRIMARY KEY,
CAPACIDAD    INT NOT NULL,
DURACION     INT NOT NULL,
LUGAR        VARCHAR(200)NOT NULL,
ID_ARTISTA   INT NOT NULL FOREIGN KEY REFERENCES ARTISTA(ID)
);

