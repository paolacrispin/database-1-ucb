




CREATE DATABASE data_ucb


USE data_ucb

CREATE TABLE ARTISTA(
ID          INT IDENTITY (1,1) NOT NULL PRIMARY KEY,
NOMBRE      VARCHAR(100)NOT NULL,
EDAD        INT NOT NULL,
TELEFONO    INT NOT NULL,
);

